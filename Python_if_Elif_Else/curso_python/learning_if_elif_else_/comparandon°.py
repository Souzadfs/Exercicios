num_1 = float(input('Digite o primeiro numero: '))
num_2 = float(input('Digite o segundo numero: '))

if num_1 > num_2:
    print(f' O primeiro {num_1} é maior que segundo numero {num_2}')
elif num_2 > num_1:
    print(f'O segundo numero {num_2} é maior que o primeiro. ')
else:
    print(f'O primeiro numero {num_1} e o segundo numero {num_2} são iguais.') 